package org.example.class4;

public class E10ForLoops {
    public static void main(String[] args) {



        for (int counter = 0; counter < 10; counter++) {
            System.out.println(counter);
        }

        int counter = 0;
        while (counter < 10) {
            System.out.println(counter);
            counter++;
        }
    }
}
